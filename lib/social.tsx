export function getSocialMeta(url: string): SocialMeta {
  const lower = url.toLowerCase();

  if (lower.includes('t.me') || lower.includes('telegram.me') || lower.includes('telegram.org')) {
    return { label: 'Telegram', bgColor: 'bg-blue-500 hover:bg-blue-600', icon: <TelegramIcon /> };
  }
  if (lower.includes('linkedin.com')) {
    return { label: 'LinkedIn', bgColor: 'bg-blue-700 hover:bg-blue-800', icon: <LinkedInIcon /> };
  }
  if (lower.includes('twitter.com') || lower.includes('x.com')) {
    return { label: 'X (Twitter)', bgColor: 'bg-gray-800 hover:bg-black', icon: <XIcon /> };
  }
  if (lower.includes('github.com')) {
    return { label: 'GitHub', bgColor: 'bg-gray-700 hover:bg-gray-900', icon: <GitHubIcon /> };
  }
  if (lower.includes('youtube.com') || lower.includes('youtu.be')) {
    return { label: 'YouTube', bgColor: 'bg-red-600 hover:bg-red-700', icon: <YouTubeIcon /> };
  }
  if (lower.includes('instagram.com')) {
    return { label: 'Instagram', bgColor: 'bg-pink-600 hover:bg-pink-700', icon: <InstagramIcon /> };
  }
  if (lower.includes('facebook.com') || lower.includes('fb.com')) {
    return { label: 'Facebook', bgColor: 'bg-blue-600 hover:bg-blue-700', icon: <FacebookIcon /> };
  }

  return {
    label: 'Link',
    bgColor: 'bg-gray-500 hover:bg-gray-600',
    icon: <Link size={20} className="text-white" />,
  };
}
